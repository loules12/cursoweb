function mostraAsQuatroOperacoes(num1, num2) {
    console.log(`A soma de ${num1} e ${num2} é ${num1 + num2}
    A subtração dos números ${num1} e ${num2} é ${num1 - num2}
    A multiplicação dos números ${num1} e ${num2} é ${num1 * num2}
    E a divisão dos números ${num1} e ${num2} é ${num1 / num2}`)
}

mostraAsQuatroOperacoes(5, 5)